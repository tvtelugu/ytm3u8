How To Use Script?<br/>

=============================================================

Just Redirect To Original Youtube HLS Live Link

stream.m3u8?c=<REPLACE_CHANNEL_ID_HERE>
    
<i>for example: <br/>This is tv5-News-Live Channel URL: https://www.youtube.com/@tv5news<br/>So in this case Channel ID will be - @tv5news<br/>So access <u>stream.m3u8?c=@tv5news</u></i>

stream.m3u8?v=<REPLACE_VIDEO_ID_HERE>
    
<i>for example: <br/>This is random Youtube Live URL : https://www.youtube.com/watch?v=II_m28Bm-iM<br/> So in this case Video ID wiil be - II_m28Bm-iM <br/>So access like this <u>stream.m3u8?v=II_m28Bm-iM</u></i>


